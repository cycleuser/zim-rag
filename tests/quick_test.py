def main():
    """主函数"""
    print("\n" + "=" * 60)
    print("ZIM-RAG 快速测试套件")
    print("=" * 60)
    print(f"\n开始时间：{__import__('datetime').datetime.now().strftime('%Y-%m-%d %H:%M:%S')}\n")

    tests = [
        ("API 初始化", test_api_initialization),
        ("ToolResult", test_tool_result),
        ("健康检查", test_health_check),
        ("系统统计", test_statistics),
        ("无 ZIM 提问", test_ask_without_zim),
        ("Markdown 生成", generate_sample_markdown),
    ]

    results = []
    for name, test_func in tests:
        try:
            result = test_func()
            results.append((name, result))
        except Exception as e:
            print(f"\n❌ {name} 测试异常：{e}")
            results.append((name, False))

    # 输出摘要
    print("\n" + "=" * 60)
    print("测试摘要")
    print("=" * 60)

    passed = sum(1 for _, r in results if r)
    total = len(results)

    for name, result in results:
        status = "✅" if result else "❌"
        print(f"{status} {name}")

    print(f"\n总计：{passed}/{total} 测试通过 ({passed / total * 100:.1f}%)")
    print(f"\n结束时间：{__import__('datetime').datetime.now().strftime('%Y-%m-%d %H:%M:%S')}")
    print("=" * 60 + "\n")

    return 0 if passed == total else 1


if __name__ == "__main__":
    sys.exit(main())
