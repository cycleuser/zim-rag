#!/usr/bin/env python3
"""
ZIM-RAG 简单测试脚本

快速测试 ZIM-RAG 系统功能，无需实际 ZIM 文件。
用于演示和开发测试。

使用方法:
    python tests/quick_test.py
"""

import sys
from pathlib import Path

# 添加项目路径
sys.path.insert(0, str(Path(__file__).parent.parent))

from zimrag.api import ZIMRAGAPI, ToolResult


def test_api_initialization():
    """测试 API 初始化"""
    print("=" * 60)
    print("测试 1: API 初始化")
    print("=" * 60)

    try:
        api = ZIMRAGAPI(model="llama3", index_dir="./test_index")
        print("✅ API 初始化成功")
        print(f"   模型：{api.engine.model}")
        print(f"   索引目录：{api.engine.index_dir}")
        return True
    except Exception as e:
        print(f"❌ API 初始化失败：{e}")
        return False


def test_tool_result():
    """测试 ToolResult 数据类"""
    print("\n" + "=" * 60)
    print("测试 2: ToolResult 工具结果")
    print("=" * 60)

    # 成功结果
    success = ToolResult(
        success=True,
        data={"answer": "Test answer", "sources": ["Source 1"]},
        metadata={"time": 1.5},
    )

    print("✅ 成功结果:")
    print(f"   success: {success.success}")
    print(f"   data: {success.data}")
    print(f"   metadata: {success.metadata}")
    print(f"   to_dict: {success.to_dict()}")

    # 失败结果
    failure = ToolResult(success=False, error="Test error message")

    print("\n✅ 失败结果:")
    print(f"   success: {failure.success}")
    print(f"   error: {failure.error}")
    print(f"   data: {failure.data}")

    return True


def test_health_check():
    """测试健康检查"""
    print("\n" + "=" * 60)
    print("测试 3: 健康检查")
    print("=" * 60)

    api = ZIMRAGAPI(model="llama3", index_dir="./test_index")
    result = api.check_health()

    if result.success:
        print("✅ 健康检查通过")
        for key, value in result.data.items():
            print(f"   {key}: {value}")
    else:
        print(f"⚠️  健康检查警告：{result.error}")
        if result.data:
            for key, value in result.data.items():
                print(f"   {key}: {value}")

    return True


def test_statistics():
    """测试统计信息"""
    print("\n" + "=" * 60)
    print("测试 4: 系统统计")
    print("=" * 60)

    api = ZIMRAGAPI(model="llama3", index_dir="./test_index")
    result = api.get_stats()

    if result.success:
        print("✅ 统计信息:")
        for key, value in result.data.items():
            print(f"   {key}: {value}")
    else:
        print(f"❌ 获取统计失败：{result.error}")

    return True


def test_ask_without_zim():
    """测试无 ZIM 文件时的提问"""
    print("\n" + "=" * 60)
    print("测试 5: 无 ZIM 文件提问")
    print("=" * 60)

    api = ZIMRAGAPI(model="llama3", index_dir="./test_index")
    result = api.ask(question="测试问题", context_limit=3)

    if result.success:
        print("✅ 回答生成成功")
        print(f"   答案：{result.data.get('answer', 'N/A')[:100]}...")
        print(f"   来源数：{len(result.data.get('sources', []))}")
        print(f"   生成时间：{result.data.get('generation_time', 0):.2f}s")
    else:
        print(f"⚠️  回答生成失败（预期）: {result.error}")

    return True


